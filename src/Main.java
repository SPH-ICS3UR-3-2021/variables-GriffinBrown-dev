public class Main {
    public static void main(String[] args){
        int myInt = 13;
        System.out.println("Integer Variable: " + myInt);
        double myDouble = 1.3;
        System.out.println("Double Variable: " + myDouble);
        float myFloat = 59.6f;
        System.out.println("Float Variable: " + myFloat);
        char myChar = 'F';
        System.out.println("Character Variable: " + myChar);
        String myString = "MmmmmmmmmmmmmBEEP";
        System.out.println("String Variable: " + myString);
        boolean myBool = false;
        System.out.println("Boolean Variable: " + myBool);
    }
    
}
